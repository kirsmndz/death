#include <stdio.h>
#include <stdlib.h>

int main()
{
    int n;
    printf("\nFor loop example - Incrementation");
    for (n = 1; n <= 5; n++)
    {
        printf("\n%d", n);
    }
    printf("\n");
    return 0;
}